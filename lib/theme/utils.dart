import "package:flutter/material.dart";

import "app_theme.dart";

extension OverrideTextThemeStylesX on BuildContext {
  /// Add here more arguments if you need to override some other styles
  ThemeData defaultThemeWithOverrideTextStyles({
    TextStyle? title,
    TextStyle? titleWhite,
    TextStyle? bodyWhite,
  }) =>
      Theme.of(this).copyWith(
        extensions: [
          appTheme.copyWith(
            textTheme: textTheme.copyWith(
              title: title,
              titleWhite: titleWhite,
              bodyWhite: bodyWhite,
            ),
          ),
        ],
      );
}
